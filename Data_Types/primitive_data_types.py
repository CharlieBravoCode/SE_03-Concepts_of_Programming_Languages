# Integer
my_integer = 42
print(my_integer)  # Output: 42

# Floating-point
my_float = 3.14
print(my_float)  # Output: 3.14

# Boolean
my_boolean = True
print(my_boolean)  # Output: True

# Character (In Python, characters are represented as strings of length 1)
my_char = 'A'
print(my_char)  # Output: A